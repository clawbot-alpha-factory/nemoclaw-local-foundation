# -- LLM Helpers -----------------------------------------------------------


def call_llm(messages, max_tokens=4000):
    from lib.routing import call_llm as _routed_call
    return _routed_call(messages, task_class="moderate", max_tokens=max_tokens)


def call_cheap_critic(output_text, task_description):
    messages = [
        ("system", "You are a quality evaluator. Score the output 1-10 on: task completion, correctness, usefulness, structure. Return ONLY a JSON object: {\"score\": N, \"feedback\": \"...\"}"),
        ("human", f"Task: {task_description}\n\nOutput to evaluate:\n{output_text[:2000]}"),
    ]
    try:
        from langchain_anthropic import ChatAnthropic
        from lib.routing import resolve_alias as _ra
        _cp, _cm, _cc = _ra("structured_short")
        llm = ChatAnthropic(model=_cm, max_tokens=300)
        resp = llm.invoke(messages)
        text = resp.content.strip()
        if "{" in text:
            json_str = text[text.index("{"):text.rindex("}") + 1]
            data = json.loads(json_str)
            return data.get("score", 5), data.get("feedback", "")
    except Exception:
        pass
    return 5.0, "Critic unavailable"


def estimate_cost(task_class="moderate"):
    from lib.routing import estimate_cost as _est
    return _est(task_class)


# -- Skill Spec ------------------------------------------------------------


def load_spec(args):
    spec = {
        "inputs": {},
        "context": {
            "workflow_id": args.thread_id or f"skill-{SKILL_ID}-{datetime.now(timezone.utc).strftime('%Y%m%d-%H%M%S')}-{os.urandom(4).hex()}",
            "budget_state": {"remaining": 20.0},
            "step_history": [],
        },
    }
    if args.input:
        for pair in args.input:
            spec["inputs"][pair[0]] = pair[1]
    if args.input_from:
        envelope_path = Path(args.input_from)
        if envelope_path.exists():
            envelope = json.loads(envelope_path.read_text())
            spec["inputs"].update(envelope.get("outputs", {}))
    return spec


def validate_inputs(spec):
    errors = []
    if not spec["inputs"].get("client_data") or len(spec["inputs"]["client_data"]) < 20:
        errors.append("Missing required input: client_data (min 20 chars)")
    if not spec["inputs"].get("report_period"):
        errors.append("Missing required input: report_period")
    if errors:
        print("ERROR: Input validation failed:")
        for e in errors:
            print(f"  {e}")
        sys.exit(1)


# -- Steps -----------------------------------------------------------------


def step_1_parse(state):
    client_data = spec["inputs"].get("client_data", "")
    report_period = spec["inputs"].get("report_period", "last 7 days")
    include_sections = spec["inputs"].get("include_sections", "all")
    tone = spec["inputs"].get("tone", "professional")

    context = f"""Task: Weekly Client Report Generation
- report_period: {report_period}
- include_sections: {include_sections}
- tone: {tone}
- client_data: {client_data}"""

    return {**state, "step_1_output": context}


def step_2_executive_summary(state):
    context = state["step_1_output"]
    feedback = state.get("critic_feedback", "")

    feedback_section = ""
    if feedback:
        feedback_section = f"\n\nPrevious attempt feedback (improve on this):\n{feedback}"

    messages = [
        ("system", "You are an expert client success manager who produces clear, data-driven weekly reports that demonstrate value and build client confidence."),
        ("human", f"""{context}{feedback_section}

Generate the Executive Summary section of the weekly client report (2-3 paragraphs):
## Executive Summary
- What was delivered this period (specific tasks, outputs, campaigns)
- Key wins and achievements (metrics, milestones, positive outcomes)
- Any issues encountered and how they were resolved
- Overall account health assessment"""),
    ]

    from lib.routing import resolve_from_env_or_config as _resolve
    provider, model, _cost = _resolve("moderate")
    cost = estimate_cost()
    print(f"    [budget] alias=moderate model={model} cost=${cost} remaining=${state['context']['budget_state']['remaining']:.3f}")

    content, error = call_llm(messages)
    if error or not content:
        return {**state, "error": str(error or "Empty LLM response")}

    state["context"]["budget_state"]["remaining"] -= cost
    return {**state, "generated_output": content, "cost": state.get("cost", 0) + cost}


def step_3_detailed_metrics(state):
    prev_output = state.get("generated_output", "")
    context = state["step_1_output"]

    messages = [
        ("system", "You are an expert data analyst who presents metrics clearly with context and trend analysis."),
        ("human", f"""{context}

Executive summary so far:
{prev_output[:2000]}

Generate the Detailed Metrics section:
## Tasks & Deliverables
(Tasks completed, skills executed, deliverables produced — with counts and categories)
## Financial Performance
(Costs incurred, revenue attributed, ROI calculation, budget utilization)
## SLA Compliance
(Uptime, response times, delivery timelines, quality scores)
## Engagement Metrics
(Client interactions, feedback received, satisfaction indicators)
## Period-over-Period Comparison
(This week vs last week — improvements, regressions, trends)"""),
    ]

    cost = estimate_cost()
    content, error = call_llm(messages, max_tokens=4000)
    if error or not content:
        return {**state, "error": str(error or "Empty LLM response")}

    combined = prev_output + "\n\n---\n\n" + content
    state["context"]["budget_state"]["remaining"] -= cost
    return {**state, "generated_output": combined, "cost": state.get("cost", 0) + cost}


