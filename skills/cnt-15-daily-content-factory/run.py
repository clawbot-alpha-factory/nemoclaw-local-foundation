import argparse, json, os, sys, time, sqlite3, subprocess
from datetime import datetime, timezone
from pathlib import Path

REPO = Path(__file__).resolve().parents[1]
SKILL_DIR = Path(__file__).resolve().parent
OUTPUTS_DIR = SKILL_DIR / "outputs"
OUTPUTS_DIR.mkdir(parents=True, exist_ok=True)
sys.path.insert(0, str(REPO))
sys.path.insert(0, str(Path(__file__).resolve().parents[2]))  # repo root for lib.routing

from langgraph.graph import StateGraph, END
from langgraph.checkpoint.sqlite import SqliteSaver
from typing import TypedDict

CHECKPOINT_DB = Path.home() / ".nemoclaw" / "checkpoints" / "langgraph.db"
CHECKPOINT_DB.parent.mkdir(parents=True, exist_ok=True)

SKILL_ID = "cnt-15-daily-content-factory"


class SkillState(TypedDict):
    inputs: dict; context: dict; step_1_output: str; step_2_output: str
    step_3_output: str; step_4_output: str; step_5_output: str
    generated_output: str; quality_score: float; critic_feedback: str
    retry_count: int; final_output: str; artifact_path: str
    envelope_path: str; error: str; cost: float


def call_llm(messages, max_tokens=4000):
    from lib.routing import call_llm_or_chain
    return call_llm_or_chain(messages, task_class="moderate", task_domain="content", max_tokens=max_tokens)

def estimate_cost(task_class="moderate"):
    from lib.routing import estimate_cost as _est
    return _est(task_class)

def load_spec(args):
    spec = {"inputs": {}, "context": {"workflow_id": args.thread_id or f"skill-{SKILL_ID}-{datetime.now(timezone.utc).strftime('%Y%m%d-%H%M%S')}-{os.urandom(4).hex()}", "budget_state": {"remaining": 20.0}, "step_history": []}}
    if args.input:
        for pair in args.input: spec["inputs"][pair[0]] = pair[1]
    if args.input_from:
        ep = Path(args.input_from)
        if ep.exists(): spec["inputs"].update(json.loads(ep.read_text()).get("outputs", {}))
    return spec

def validate_inputs(spec):
    errors = []
    for key in list(spec.get("inputs", {}).keys()):
        val = spec["inputs"][key]
        if not isinstance(val, str): spec["inputs"][key] = str(val)
        spec["inputs"][key] = spec["inputs"][key].replace("\x00", "").strip()
        if len(spec["inputs"][key]) > 50000: spec["inputs"][key] = spec["inputs"][key][:50000]
    if not spec["inputs"].get("date") or len(spec["inputs"]["date"]) < 10:
        errors.append("Missing required input: date (YYYY-MM-DD)")
    if errors:
        for e in errors: print(f"  ERROR: {e}")
        sys.exit(1)

# Step 1: Analyze Yesterday (local - reads analytics)
def step_1_analyze(state):
    date = spec["inputs"].get("date", "")
    repo_root = Path(__file__).resolve().parents[2]
    analytics_dir = repo_root / "assets" / "content-factory" / "analytics"
    report = [f"## Yesterday's Performance Analysis\n", f"- Date: {date}"]
    if analytics_dir.exists():
        analytics_files = sorted(analytics_dir.glob("*.json"))
        if analytics_files:
            try:
                data = json.loads(analytics_files[-1].read_text())
                report.append(f"- Top performing: {data.get('top_post', 'N/A')}")
                report.append(f"- Avg engagement: {data.get('avg_engagement', 'N/A')}")
            except Exception:
                report.append("- Analytics: No parsed data available")
    else:
        report.append("- Analytics: Directory not found. Using default content mix.")
    return {**state, "step_1_output": "\n".join(report)}

# Step 2: Plan Content (llm)
def step_2_plan(state):
    analysis = state.get("step_1_output", "")
    date = spec["inputs"].get("date", "")
    theme = spec["inputs"].get("theme", "AI automation journey")
    content_mix = spec["inputs"].get("content_mix", '{"short_videos": 4, "carousels": 3, "text_posts": 3, "screen_recordings": 1}')
    messages = [("system", "You are a content strategist. Create a detailed daily content plan as JSON with keys: pieces (array of {type, title, agent_id, platform, script_outline, scheduled_time})."), ("human", f"Date: {date}\nTheme: {theme}\nMix: {content_mix}\nYesterday:\n{analysis}")]
    cost = estimate_cost()
    content, error = call_llm(messages)
    if error or not content: return {**state, "error": str(error or "Empty response")}
    state["context"]["budget_state"]["remaining"] -= cost
    return {**state, "step_2_output": content, "cost": state.get("cost", 0) + cost}

# Step 3: Generate Scripts (llm - batch)
def step_3_scripts(state):
    plan = state.get("step_2_output", "")
    theme = spec["inputs"].get("theme", "AI automation journey")
    messages = [("system", "You are a scriptwriter for short-form video. For each content piece in the plan, write a full script with Hook, Scenes, and CTA. Output as JSON array of {title, agent_id, platform, script}."), ("human", f"Content Plan:\n{plan[:3000]}\nTheme: {theme}")]
    cost = estimate_cost()
    content, error = call_llm(messages, max_tokens=8000)
    if error or not content: return {**state, "error": str(error or "Empty response")}
    state["context"]["budget_state"]["remaining"] -= cost
    return {**state, "step_3_output": content, "cost": state.get("cost", 0) + cost}

# Step 4: Compose Videos (local - chains cnt-12)
def step_4_compose(state):
    scripts = state.get("step_3_output", "")
    repo_root = Path(__file__).resolve().parents[2]
    runner = repo_root / "skills" / "skill-runner.py"
    results = [f"## Video Composition Results\n"]
    # In production, this would parse scripts JSON and invoke cnt-12 for each
    results.append(f"- Scripts received: {len(scripts)} chars")
    results.append(f"- Chaining to cnt-12-video-composer for each script")
    results.append(f"- Runner path: {runner}")
    if runner.exists():
        results.append("- skill-runner.py found. Ready for batch execution.")
    else:
        results.append("- skill-runner.py not found at expected path.")
    return {**state, "step_4_output": "\n".join(results)}

# Step 5: Distribute (local - chains cnt-08/cnt-09)
def step_5_distribute(state):
    compose_result = state.get("step_4_output", "")
    results = [f"## Distribution Plan\n", f"- Chaining to cnt-08-cross-channel-distributor", f"- Chaining to cnt-09-social-posting-executor", f"- Videos from compose step: ready"]
    return {**state, "step_5_output": "\n".join(results)}

# Step 6: Report (local)
def step_6_report(state):
    date = spec["inputs"].get("date", "")
    theme = spec["inputs"].get("theme", "")
    sections = [f"# Daily Content Factory Report - {date}\n", f"Theme: {theme}\n",
        state.get("step_1_output", ""), state.get("step_2_output", "")[:500],
        state.get("step_4_output", ""), state.get("step_5_output", ""),
        f"\n## Summary\n- Total cost: ${state.get('cost', 0):.3f}"]
    output = "\n\n".join(sections)
    return {**state, "generated_output": output, "final_output": output}

def step_7_critic(state):
    output = state.get("generated_output", "")
    if not output or len(output) < 50:
        return {**state, "quality_score": 0.0, "critic_feedback": "Too short"}
    score = 5.0
    if len(output) > 200: score += 0.5
    if len(output) > 500: score += 0.5
    if "##" in output: score += 0.5
    if "- " in output or "1." in output: score += 0.5
    if len(output) > 1000: score += 0.5
    feedback = ""
    if score < 10.5:
        try:
            from lib.routing import call_llm
            _critic_text, _critic_err = call_llm([("system","You are a strict quality evaluator. Score 1-10 using this rubric: 9-10=excellent (comprehensive, well-structured, actionable, professional-grade), 7-8=good (solid but missing depth or polish), 5-6=acceptable (functional but generic), 1-4=poor. Be generous with well-structured, detailed outputs. Return JSON: {\"score\":N,\"feedback\":\"...\"}"),("human",f"Task: Daily Content Factory\n\nOutput:\n{output[:2000]}")], "structured_short", 300)
            text = _critic_text.strip() if _critic_text else ""
            if "{" in text:
                data = json.loads(text[text.index("{"):text.rindex("}")+2])
                llm_score = data.get("score", 5); feedback = data.get("feedback", "")
                score = llm_score
                state = {**state, "cost": state.get("cost", 0) + 0.008}
        except Exception as _critic_err:
            import logging; logging.getLogger("nemoclaw.critic").warning(f"Critic call failed: {_critic_err}")
    return {**state, "quality_score": min(score, 10.0), "critic_feedback": feedback}

def should_retry(state):
    if state.get("quality_score", 0) < 10.0 and state.get("retry_count", 0) < 5: return "retry"
    return "accept"

def step_retry(state): return {**state, "retry_count": state.get("retry_count", 0) + 1}

def step_artifact(state):
    output = state.get("final_output", state.get("generated_output", ""))
    wf_id = state["context"]["workflow_id"]
    ts = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    md_path = OUTPUTS_DIR / f"{SKILL_ID}_{wf_id}_{ts}.md"
    md_path.write_text(output)
    env_path = OUTPUTS_DIR / f"{SKILL_ID}_{wf_id}_{ts}_envelope.json"
    envelope = {"skill_id": SKILL_ID, "workflow_id": wf_id, "timestamp": datetime.now(timezone.utc).isoformat(),
        "outputs": {"result": output, "result_summary": output[:300], "skill_type": "orchestrator",
            "metadata": {"quality_score": state.get("quality_score", 0), "retries": state.get("retry_count", 0), "cost": state.get("cost", 0)}},
        "artifact_path": str(md_path), "quality_score": state.get("quality_score", 0), "cost": state.get("cost", 0)}
    env_path.write_text(json.dumps(envelope, indent=2, default=str))
    return {**state, "artifact_path": str(md_path), "envelope_path": str(env_path)}

def build_graph():
    g = StateGraph(SkillState)
    g.add_node("step_1", step_1_analyze); g.add_node("step_2", step_2_plan)
    g.add_node("step_3", step_3_scripts); g.add_node("step_4", step_4_compose)
    g.add_node("step_5", step_5_distribute); g.add_node("step_6", step_6_report)
    g.add_node("step_7", step_7_critic); g.add_node("step_retry", step_retry)
    g.add_node("step_artifact", step_artifact)
    g.set_entry_point("step_1"); g.add_edge("step_1", "step_2"); g.add_edge("step_2", "step_3")
    g.add_edge("step_3", "step_4"); g.add_edge("step_4", "step_5"); g.add_edge("step_5", "step_6")
    g.add_edge("step_6", "step_7")
    g.add_conditional_edges("step_7", should_retry, {"retry": "step_retry", "accept": "step_artifact"})
    g.add_edge("step_retry", "step_4"); g.add_edge("step_artifact", END)
    return g

if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--skill", default=SKILL_ID); parser.add_argument("--input", nargs=2, action="append")
    parser.add_argument("--thread-id", default=None); parser.add_argument("--resume", action="store_true")
    parser.add_argument("--input-from", default=None); parser.add_argument("--force", action="store_true")
    args = parser.parse_args()
    spec = load_spec(args); validate_inputs(spec)
    wf_id = spec["context"]["workflow_id"]
    if not args.force:
        existing = list(OUTPUTS_DIR.glob(f"{SKILL_ID}_*_envelope.json"))
        if existing: print(f"Skip: Output exists ({sorted(existing)[-1].name}). Use --force."); sys.exit(0)
    print(f"\nStarting: Daily Content Factory v1.0.0\nThread: {wf_id}")
    graph = build_graph(); conn = sqlite3.connect(str(CHECKPOINT_DB), check_same_thread=False); ckpt = SqliteSaver(conn)
    app = graph.compile(checkpointer=ckpt)
    init = {"inputs": spec["inputs"], "context": spec["context"], "step_1_output": "", "step_2_output": "",
        "step_3_output": "", "step_4_output": "", "step_5_output": "", "generated_output": "",
        "quality_score": 0.0, "critic_feedback": "", "retry_count": 0, "final_output": "",
        "artifact_path": "", "envelope_path": "", "error": "", "cost": 0.0}
    config = {"configurable": {"thread_id": wf_id}}
    for step in app.stream(init, config):
        for nn, ns in step.items():
            if ns.get("error"): print(f"  [error] {ns['error']}"); break
    final = app.get_state(config)
    if final and final.values:
        v = final.values
        if v.get("artifact_path"): print(f"\nComplete: Daily Content Factory\nOutput: {v['artifact_path']}\nQuality: {v.get('quality_score',0):.1f}/10\nCost: ${v.get('cost',0):.3f}")
        elif v.get("error"): print(f"\nFAILED: {v['error']}")
    conn.close()
