def step_2_research_keywords(state):
    context = state["step_1_output"]
    feedback = state.get("critic_feedback", "")

    feedback_section = ""
    if feedback:
        feedback_section = f"\n\nPrevious attempt feedback (improve on this):\n{feedback}"

    messages = [
        ("system", "You are an expert SEO strategist and keyword researcher specializing in competitive analysis, search intent mapping, and content gap identification."),
        ("human", f"""{context}{feedback_section}

Research keywords for this niche. Produce a comprehensive keyword research report:
## Primary Keywords
(10-15 high-value primary keywords with estimated search volume, difficulty score 1-100, and search intent type)
## Long-Tail Keywords
(20-30 long-tail keyword opportunities with lower competition)
## Search Intent Mapping
(Map keywords to intent: informational, navigational, commercial, transactional)
## Keyword Difficulty Analysis
(Difficulty tiers: easy, medium, hard — with recommendations per tier)"""),
    ]

    from lib.routing import resolve_from_env_or_config as _resolve
    provider, model, _cost = _resolve("moderate")
    cost = estimate_cost()
    print(f"    [budget] alias=moderate model={model} cost=${cost} remaining=${state['context']['budget_state']['remaining']:.3f}")

    content, error = call_llm(messages)
    if error or not content:
        return {**state, "error": str(error or "Empty LLM response")}

    state["context"]["budget_state"]["remaining"] -= cost
    return {**state, "generated_output": content, "cost": state.get("cost", 0) + cost}


def step_3_analyze_competition(state):
    prev_output = state.get("generated_output", "")
    context = state["step_1_output"]

    messages = [
        ("system", "You are an expert SEO strategist specializing in SERP analysis and competitive intelligence."),
        ("human", f"""{context}

Previous keyword research:
{prev_output[:3000]}

Now analyze the competitive landscape:
## SERP Analysis
(Top 10 results analysis for primary keywords — domain authority, content type, word count, backlink estimates)
## Content Gaps
(Topics competitors miss or cover poorly — specific opportunities)
## Low-Competition Opportunities
(Keywords with high value but low competition — quick-win targets)
## Competitor Weaknesses
(Where competitor content is thin, outdated, or poorly optimized)"""),
    ]

    cost = estimate_cost()
    content, error = call_llm(messages, max_tokens=4000)
    if error or not content:
        return {**state, "error": str(error or "Empty LLM response")}

    combined = prev_output + "\n\n---\n\n" + content
    state["context"]["budget_state"]["remaining"] -= cost
    return {**state, "generated_output": combined, "cost": state.get("cost", 0) + cost}


