def load_spec(args):
    spec = {
        "inputs": {},
        "context": {
            "workflow_id": args.thread_id or f"skill-{SKILL_ID}-{datetime.now(timezone.utc).strftime('%Y%m%d-%H%M%S')}-{os.urandom(4).hex()}",
            "budget_state": {"remaining": 20.0},
            "step_history": [],
        },
    }
    if args.input:
        for pair in args.input:
            spec["inputs"][pair[0]] = pair[1]
    if args.input_from:
        envelope_path = Path(args.input_from)
        if envelope_path.exists():
            envelope = json.loads(envelope_path.read_text())
            spec["inputs"].update(envelope.get("outputs", {}))
    return spec


def validate_inputs(spec):
    errors = []
    if not spec["inputs"].get("dispute_context") or len(spec["inputs"]["dispute_context"]) < 20:
        errors.append("Missing required input: dispute_context (min 20 chars)")
    allowed_types = ["refund_request", "partial_refund", "credit_note", "chargeback", "billing_error"]
    if not spec["inputs"].get("dispute_type") or spec["inputs"]["dispute_type"] not in allowed_types:
        errors.append(f"Missing or invalid input: dispute_type (must be one of {allowed_types})")
    if not spec["inputs"].get("client_info") or len(spec["inputs"]["client_info"]) < 5:
        errors.append("Missing required input: client_info")
    if errors:
        print("ERROR: Input validation failed:")
        for e in errors:
            print(f"  {e}")
        sys.exit(1)


