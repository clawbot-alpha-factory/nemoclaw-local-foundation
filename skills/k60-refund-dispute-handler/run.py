def step_1_parse(state):
    dispute_context = spec["inputs"].get("dispute_context", "")
    dispute_type = spec["inputs"].get("dispute_type", "")
    client_info = spec["inputs"].get("client_info", "")
    amount_usd = spec["inputs"].get("amount_usd", "")
